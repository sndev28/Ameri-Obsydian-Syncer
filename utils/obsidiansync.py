class ObsidianSync:
    def __init__(self, vault_path, git_path, sleep_time):
        self.sleep_time = sleep_time
        self.vault_path = vault_path
        self.vault_git_path = git_path
        os.chdir(self.vault_path)

    def isSync(self):
        output = subprocess.check_output("git status", shell=True)
        output_remote = subprocess.check_output("git pull", shell=True)
        if "On branch master\nnothing to commit, working tree clean\n" in output.decode("utf-8"):
            print("Synced")
            return True
        else:
            print(output)
            return False

    def sync(self):
        if self.isSync():
            return
        
        os.system("git add .")
        os.system(f"git commit -m \"Automatic Synced by ObsidianSync : ver.{datetime.now().strftime('%Y%m%d_%H%M')}\"")
        os.system("git add .")
        os.system(f"git push -u {self.vault_git_path} master")

        print("Synced")

    def syncStart(self, mode):
        if mode == "Manual":
            self.sync()
            return
        
        while True:
            self.sync()
            sleep(self.sleep_time*60)




